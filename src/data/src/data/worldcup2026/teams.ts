export function getWorldCupTeamByName(
  teamName: string
): WorldCupTeam | undefined {
  const normalized = teamName.trim().toLowerCase();

  return worldCup2026Teams.find((team) => {
    const namesToCheck = [team.name, team.fifaCode, ...(team.aliases ?? [])];

    return namesToCheck.some(
      (name) => name.trim().toLowerCase() === normalized
    );
  });
}

export function getTeamsByGroup(group: WorldCupTeam['group']): WorldCupTeam[] {
  return worldCup2026Teams.filter((team) => team.group === group);
}

